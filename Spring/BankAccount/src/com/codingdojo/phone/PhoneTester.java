package com.codingdojo.phone;

public class PhoneTester {

	public static void main(String[] args) {
		Galaxy galaxyPhone = new Galaxy("S9", 99, "Verizon", "Ring Ring Ring!");
		iPhone iPhoneInstance = new iPhone("X", 100, "AT&T", "Zing");
		
		galaxyPhone.displayInfo();
		System.out.println(galaxyPhone.ring());
		System.out.println(galaxyPhone.unlock());
		
		iPhoneInstance.displayInfo();
		System.out.println(iPhoneInstance.ring());
		System.out.println(iPhoneInstance.unlock());

	}

}
